#include <stdio.h>
#include <stdlib.h>
#include <string.h>
int curr = 0;
char str[100];
// -----------------------------------------------------------------------------------------------------------------------
void T();
void Tprime();
void S();
// -----------------------------------------------------------------------------------------------------------------------
void invalid(){
	printf("-----------------ERROR!----------------\n");
	exit(0);
}

void valid(){
	printf("----------------SUCCESS!---------------\n");
	exit(0);
}
// -----------------------------------------------------------------------------------------------------------------------
void S(){
	if(str[curr]=='a' || str[curr]=='>'){
		curr++;
	}
	else if(str[curr]=='('){
		curr++;
		T();
		if(str[curr]==')'){
			curr++;
		}
		else{
			invalid();
		}	
	}
}
// -----------------------------------------------------------------------------------------------------------------------

void Tprime(){
	if(str[curr]==','){
		curr++;
		S();
		Tprime();
	}
}
// -----------------------------------------------------------------------------------------------------------------------

void T(){
	S();
	Tprime();
}
// -----------------------------------------------------------------------------------------------------------------------

int main(){
	printf("Enter String: ");
	scanf("%s", str);
	S();
	if(str[curr] == '$')
		valid();
	else
		// printf("%c\n", str[curr]);
		invalid();
}
